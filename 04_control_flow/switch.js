const month = 3

switch (month) {
    case 1:
        console.log("jan");
        break;
    case 2:
        console.log("feb");
        break;
    case 3:
        console.log("march");
        break;
    case 4:
        console.log("Aprin");
        break;

    default:
        console.log("no one value is matched")
        break;
};

